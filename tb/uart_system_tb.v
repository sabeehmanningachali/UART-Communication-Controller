`timescale 1ns / 1ps

module uart_system_tb;

    //----------------------------------------------------------
    // Inputs
    //----------------------------------------------------------

    reg sys_clk;
    reg rst_n;

    reg tx_enable;
    reg [7:0] tx_data_in;
    reg even_odd;

    //----------------------------------------------------------
    // Outputs
    //----------------------------------------------------------

    wire [7:0] parallel_data_out;
    wire data_valid;

    wire tx_busy;
    wire rx_busy;

    //----------------------------------------------------------
    // DUT
    //----------------------------------------------------------

    uart_system uut
    (
        .sys_clk(sys_clk),
        .rst_n(rst_n),

        .tx_enable(tx_enable),
        .tx_data_in(tx_data_in),
        .even_odd(even_odd),

        .parallel_data_out(parallel_data_out),
        .data_valid(data_valid),

        .tx_busy(tx_busy),
        .rx_busy(rx_busy)
    );

    //----------------------------------------------------------
    // 64 MHz System Clock
    //----------------------------------------------------------

    initial
    begin
        sys_clk = 1'b0;
        forever #8 sys_clk = ~sys_clk;
    end

    //----------------------------------------------------------
    // VCD Dump
    //----------------------------------------------------------

    initial
    begin
        $dumpfile("uart_system.vcd");
        $dumpvars(0, uart_system_tb);
    end

    //----------------------------------------------------------
    // Stimulus
    //----------------------------------------------------------

    initial
    begin

        rst_n      = 0;
        tx_enable  = 0;
        tx_data_in = 8'h00;
        even_odd   = 1'b1;      // Odd parity

        //------------------------------------------------------
        // Reset
        //------------------------------------------------------

        #50;
        rst_n = 1;

        //------------------------------------------------------
        // Frame 1 : 0xAA
        //------------------------------------------------------

        #100;

        tx_data_in = 8'hAA;
        tx_enable  = 1;

        @(posedge tx_busy);
        tx_enable = 0;

        @(negedge tx_busy);

        //------------------------------------------------------
        // Gap
        //------------------------------------------------------

        #500;

        //------------------------------------------------------
        // Frame 2 : 0x55
        //------------------------------------------------------

        tx_data_in = 8'h55;
        tx_enable  = 1;

        @(posedge tx_busy);
        tx_enable = 0;

        @(negedge tx_busy);

        //------------------------------------------------------
        // Gap
        //------------------------------------------------------

        #500;

        //------------------------------------------------------
        // Frame 3 : 0xCC
        //------------------------------------------------------

        tx_data_in = 8'hCC;
        tx_enable  = 1;

        @(posedge tx_busy);
        tx_enable = 0;

        @(negedge tx_busy);

        //------------------------------------------------------
        // Gap
        //------------------------------------------------------

        #500;

        //------------------------------------------------------
        // Frame 4 : 0xF0
        //------------------------------------------------------

        tx_data_in = 8'hF0;
        tx_enable  = 1;

        @(posedge tx_busy);
        tx_enable = 0;

        @(negedge tx_busy);

        //------------------------------------------------------
        // Wait for receiver
        //------------------------------------------------------

        #2000;

        $finish;

    end

endmodule